public class Calculator {

    public double decrease(double i, double i1) {
        return i-i1;
    }

    public double mul(double i, double i1) {
        return i*i1;
    }

    public double addplus(double i, double i1) {
        return i+i1;
    }

    public double divide(double i, double i1) { return i/i1; }
}
